public class e3c {

    public static void main(String[] args) {
        double dFah, dCel;

        for (dCel = -10; dCel <= 10; dCel++) {
            dFah = (dCel * 9) / 5 + 32;
            System.out.println(dCel + " Celcius in Fahrenheit is " + dFah);
        }


    }
}
